package _1_learn._1_1_basics._1_1_15_math

import kotlin.math.*;

fun main() {
    println(".max()   : " + max(1.0, 2.0))
    println(".min()   : " + min(1.0, 2.0))
    println(".sqrt()  : " + sqrt(4.0))
    println(".pow()   : " + 3.0.pow(4.0))
    println(".random(): " + Math.random())
    print(".abs()   : " + abs(-4.0))
}